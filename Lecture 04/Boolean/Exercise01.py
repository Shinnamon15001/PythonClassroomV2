words = input("Enter a string : ")

if (words.isalnum()) != (words.isalpha()):
    print("string isalphanumeric characters")
if words.isalpha():
    print("string is alphabetic characters")
if words.islower():
    print("string is lowercase")
if words.isnumeric():
    print("string is numeric characters")
if words.istitle():
    print("string is title case")
if words.isupper():
    print("string is upper case")